    protected static Image createImage(String path, String description) {
        URL imageURL = index.class.getResource(path);
        if (imageURL == null) {
            System.out.println("Resource not found: " + path);
            return null;
        } else {
            return (new ImageIcon(imageURL, description)).getImage();
        }
    }   
    
    
